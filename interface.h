/* MISO Pin */
#define SPIM_MISO_PORT         XMC_GPIO_PORT2
#define SPIM_MISO_PIN          (0U)

/* Slave Select Pin */
#define SPIM_SEL0_PORT         XMC_GPIO_PORT0
#define SPIM_SEL0_PIN          (9U)

/* SPI Clock Pin */
#define SPIM_SCLKOUT_PORT      XMC_GPIO_PORT0
#define SPIM_SCLKOUT_PIN       (8U)

#endif

#endif /* INTERFACE_H */


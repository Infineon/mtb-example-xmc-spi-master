#include "cybsp.h"
#include "xmc_gpio.h"

/*******************************************************************************
* Macros
*******************************************************************************/
#ifdef TARGET_KIT_XMC47_RELAX_V1

/* MOSI Pin */
#define SPIM_MOSI_PORT         XMC_GPIO_PORT0
#define SPIM_MOSI_PIN          (5U)

/* MISO Pin */
#define SPIM_MISO_PORT         XMC_GPIO_PORT0
#define SPIM_MISO_PIN          (4U)

/* Slave Select Pin */
#define SPIM_SEL0_PORT         XMC_GPIO_PORT0
#define SPIM_SEL0_PIN          (6U)

/* SPI Clock Pin */
#define SPIM_SCLKOUT_PORT      XMC_GPIO_PORT0
#define SPIM_SCLKOUT_PIN       (11U)

#endif

